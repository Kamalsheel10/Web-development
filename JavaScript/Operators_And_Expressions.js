console.log("Operators in js");
let a = 45;
let b = 69;
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a ** a = ", a ** a); // This is equals to a to the power a
console.log(++a)
console.log(a++)
console.log(a)

console.log("Comparison Operators");
let compa = 69;
let compb = "69";

console.log("a == b", compa == compb);
console.log("a != b", compa != compb);

console.log("a === b", compa === compb); // The === operator returns the value false even if the string and numeric values are same
console.log("a !== b", compa !== compb);



